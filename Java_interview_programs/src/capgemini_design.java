
public class capgemini_design {
	
	/*
	 * *
	 * **
	 * ***
	 * ****
	 * ****
	 * ***
	 * **
	 * *
	 * 
	 */

	public static void main(String v[])
	{
		for (int i=1;i<=4;i++)
		{
			for (int j=1;j<=i;j++)
			{
			System.out.print("*");
			}
			System.out.println();
			
		}
		
		
		for (int i=4;i>=1;i--)
		{
			for (int j=i;j>=1;j--)
			{
			System.out.print("*");
			}
			System.out.println();
			
		}
		
		
	}
	
	
}
