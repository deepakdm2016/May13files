package ocjp;

class Shirt {

	public int getSize() {
		return 40;
	}
}

class TShirt extends Shirt {
	public int getSize() {
		return 44;
	}
}

class FullSleevShirt extends Shirt {
	public int getSize() {
		return 42;
	}
}

class PolymorphismTest{
	
	public static void main(String v[])
	{
		FullSleevShirt shirt=new FullSleevShirt();
		System.out.println(((Shirt)shirt).getSize());
	}
}
